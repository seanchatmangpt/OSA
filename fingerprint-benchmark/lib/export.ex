defmodule FingerprintBenchmark.Export do
  @moduledoc """
  Report Export — Generate benchmark reports in multiple formats.
  """

  @doc """
  Export benchmark report as JSON.
  """
  def export_json(comparison) do
    Jason.encode!(comparison, pretty: true)
  end

  @doc """
  Export benchmark report as markdown.
  """
  def export_markdown(comparison) do
    """
    # Process DNA Benchmark Report

    **Organization**: #{comparison.your_fingerprint.fingerprint_id}
    **Generated**: #{comparison.metadata.generated_at}

    ## Your Metrics

    | Metric | Your Value |
    |--------|-----------|
    | Cycle Time | #{Float.round(comparison.your_fingerprint.metrics.cycle_time_days, 1)} days |
    | Efficiency Score | #{Float.round(comparison.your_fingerprint.metrics.efficiency_score * 100, 0)}% |
    | Error Rate | #{Float.round(comparison.your_fingerprint.metrics.error_rate * 100, 1)}% |
    | Automation | #{Float.round(comparison.your_fingerprint.metrics.automation_level * 100, 0)}% |

    ## Industry Benchmark (#{comparison.metadata.industry})

    | Metric | P50 | P75 | P90 | Your Percentile |
    |--------|-----|-----|-----|-----------------|
    | Cycle Time | #{Float.round(comparison.industry_benchmark.p50_cycle_time_days, 1)}d | #{Float.round(comparison.industry_benchmark.p75_cycle_time_days, 1)}d | #{Float.round(comparison.industry_benchmark.p90_cycle_time_days, 1)}d | #{Float.round(comparison.comparison.percentiles.cycle_time_days * 100, 0)}th |
    | Efficiency | #{Float.round(comparison.industry_benchmark.median_efficiency * 100, 0)}% | - | - | #{Float.round(comparison.comparison.percentiles.efficiency_score * 100, 0)}th |

    ## Overall Rank

    **#{String.upcase(comparison.comparison.overall_rank)}**

    ## Gaps & Recommendations

    #{format_gaps(comparison.comparison.gaps)}

    ---

    *Report generated by Process DNA Benchmarking Service*
    """
  end

  @doc """
  Export benchmark report as HTML.
  """
  def export_html(comparison) do
    """
    <!DOCTYPE html>
    <html>
    <head>
      <title>Process DNA Benchmark Report</title>
      <style>
        body { font-family: system-ui; max-width: 800px; margin: 40px auto; }
        table { width: 100%; border-collapse: collapse; margin: 20px 0; }
        th, td { padding: 12px; text-align: left; border-bottom: 1px solid #ddd; }
        th { background: #f5f5f5; }
        .rank { font-size: 24px; font-weight: bold; padding: 20px; text-align: center;
                 border-radius: 8px; margin: 20px 0; }
        .rank.top_quartile { background: #10b981; color: white; }
        .rank.above_average { background: #3b82f6; color: white; }
        .rank.below_average { background: #f59e0b; color: white; }
        .rank.bottom_quartile { background: #ef4444; color: white; }
      </style>
    </head>
    <body>
      <h1>Process DNA Benchmark Report</h1>
      <p><strong>Generated:</strong> #{comparison.metadata.generated_at}</p>

      <h2>Overall Rank</h2>
      <div class="rank #{comparison.comparison.overall_rank}">
        #{String.upcase(comparison.comparison.overall_rank)}
      </div>

      <h2>Your Metrics vs Industry</h2>
      <table>
        <tr>
          <th>Metric</th>
          <th>Your Value</th>
          <th>Industry P50</th>
          <th>Your Percentile</th>
        </tr>
        <tr>
          <td>Cycle Time</td>
          <td>#{Float.round(comparison.your_fingerprint.metrics.cycle_time_days, 1)} days</td>
          <td>#{Float.round(comparison.industry_benchmark.p50_cycle_time_days, 1)} days</td>
          <td>#{Float.round(comparison.comparison.percentiles.cycle_time_days * 100, 0)}th</td>
        </tr>
        <tr>
          <td>Efficiency</td>
          <td>#{Float.round(comparison.your_fingerprint.metrics.efficiency_score * 100, 0)}%</td>
          <td>#{Float.round(comparison.industry_benchmark.median_efficiency * 100, 0)}%</td>
          <td>#{Float.round(comparison.comparison.percentiles.efficiency_score * 100, 0)}th</td>
        </tr>
      </table>

      <h2>Recommendations</h2>
      <ul>
        #{Enum.map(comparison.comparison.recommendations, fn r ->
          "<li><strong>#{r.metric}:</strong> #{r.recommendation} (#{r.expected_outcome})</li>"
        end) |> Enum.join("\n        ")}
      </ul>

      <p><em>Generated by Process DNA Benchmarking Service</em></p>
    </body>
    </html>
    """
  end

  defp format_gaps([]) do
    "*No significant gaps identified. Your process is performing well!*"
  end

  defp format_gaps(gaps) do
    gaps
    |> Enum.map(fn gap ->
      """
      ### #{String.upcase(gap.metric)} Gap (#{gap.severity})

      - **Your Value**: #{gap.your_value}
      - **Benchmark**: #{gap.benchmark_value}
      - **Gap**: +#{gap.gap_percent}%
      - **Recommendation**: #{gap.recommendation}
      - **Expected**: #{gap.estimated_improvement}
      """
    end)
    |> Enum.join("\n")
  end
end
