defmodule OptimalSystemAgent.Ggen.Templates.TypeScript do
  @moduledoc """
  Fortune 5 Layer 4: TypeScript Project Template Handler

  Generates TypeScript project structure from ODCS specification with proper
  npm configuration, TypeScript settings, and test infrastructure.

  Signal Theory: S=(code,spec,inform,elixir,module)
  """

  require Logger

  @doc """
  Render TypeScript template

  Generates:
  - package.json - npm package configuration
  - tsconfig.json - TypeScript compiler configuration
  - src/index.ts - Entry point
  - src/utils.ts - Utility functions
  - tests/index.test.ts - Test suite
  - .gitignore - Node.js specific rules

  Required variables:
    - project_name: name of the project

  Optional variables:
    - version: version number (default: 1.0.0)
    - description: project description
    - author: author name
  """
  def render(_template, variables) do
    project_name = Map.fetch!(variables, "project_name")
    version = Map.get(variables, "version", "1.0.0")
    description = Map.get(variables, "description", "")
    author = Map.get(variables, "author", "")

    with :ok <- validate_project_name(project_name) do
      files = [
        {"package.json", package_json(project_name, version, description, author)},
        {"tsconfig.json", tsconfig_json()},
        {"src/index.ts", index_ts(project_name)},
        {"src/utils.ts", utils_ts()},
        {"tests/index.test.ts", test_ts(project_name)},
        {".gitignore", gitignore()},
        {"README.md", readme(project_name, description)},
        {".npmrc", npmrc()}
      ]

      {:ok, files}
    end
  end

  # Private

  defp validate_project_name(name) do
    if String.match?(name, ~r/^[a-zA-Z0-9_\-\.\/]+$/) do
      :ok
    else
      {:error, "Project name contains invalid characters: #{name}"}
    end
  end

  defp package_json(project_name, version, description, author) do
    package = %{
      "name" => project_name,
      "version" => version,
      "description" => description,
      "author" => author,
      "license" => "MIT",
      "main" => "dist/index.js",
      "types" => "dist/index.d.ts",
      "files" => ["dist"],
      "scripts" => %{
        "build" => "tsc",
        "test" => "jest",
        "test:watch" => "jest --watch",
        "test:coverage" => "jest --coverage",
        "lint" => "eslint src --ext .ts",
        "format" => "prettier --write src",
        "dev" => "ts-node src/index.ts"
      },
      "devDependencies" => %{
        "@types/jest" => "^29.5.0",
        "@types/node" => "^20.0.0",
        "@typescript-eslint/eslint-plugin" => "^6.0.0",
        "@typescript-eslint/parser" => "^6.0.0",
        "eslint" => "^8.0.0",
        "jest" => "^29.5.0",
        "prettier" => "^3.0.0",
        "ts-jest" => "^29.1.0",
        "ts-node" => "^10.9.0",
        "typescript" => "^5.0.0"
      },
      "engines" => %{
        "node" => ">=16.0.0"
      }
    }

    Jason.encode!(package, pretty: true)
  end

