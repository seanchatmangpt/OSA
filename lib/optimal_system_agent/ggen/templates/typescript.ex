  end

  defp index_ts(project_name) do
    """
    /**
     * @file Main entry point for #{project_name}
     * @author Generated by ggen
     */

    import { greet, add } from './utils';

    /**
     * Main function
     */
    async function main(): Promise<void> {
        console.log(greet('World'));
        console.log(`2 + 3 = ${add(2, 3)}`);
    }

    // Run main function
    main().catch((err) => {
        console.error('Error:', err);
        process.exit(1);
    });

    export { main };
    """
  end

  defp utils_ts do
    """
    /**
     * @file Utility functions
     */

    /**
     * Greet someone by name
     * @param name - The person's name
     * @returns A greeting message
     */
    export function greet(name: string): string {
        return `Hello, ${name}!`;
    }

    /**
     * Add two numbers
     * @param a - First number
     * @param b - Second number
     * @returns The sum
     */
    export function add(a: number, b: number): number {
        return a + b;
    }

    /**
     * Subtract two numbers
     * @param a - First number
     * @param b - Second number
     * @returns The difference
     */
    export function subtract(a: number, b: number): number {
        return a - b;
    }

    /**
     * Multiply two numbers
     * @param a - First number
     * @param b - Second number
     * @returns The product
     */
    export function multiply(a: number, b: number): number {
        return a * b;
    }

    /**
     * Divide two numbers
     * @param a - Dividend
     * @param b - Divisor
     * @returns The quotient
     * @throws Error if divisor is zero
     */
    export function divide(a: number, b: number): number {
        if (b === 0) {
            throw new Error('Division by zero');
        }
        return a / b;
    }
    """
  end

  defp test_ts(_project_name) do
    """
    import { greet, add, subtract, multiply, divide } from '../src/utils';

    describe('utils', () => {
        describe('greet', () => {
            it('should greet a person', () => {
                expect(greet('Alice')).toBe('Hello, Alice!');
            });

            it('should handle empty names', () => {
                expect(greet('')).toBe('Hello, !');
            });
        });

        describe('arithmetic functions', () => {
            it('should add two numbers', () => {
                expect(add(2, 3)).toBe(5);
                expect(add(0, 0)).toBe(0);
                expect(add(-1, 1)).toBe(0);
            });

            it('should subtract two numbers', () => {
                expect(subtract(5, 3)).toBe(2);
                expect(subtract(0, 0)).toBe(0);
                expect(subtract(1, 2)).toBe(-1);
            });

            it('should multiply two numbers', () => {
                expect(multiply(2, 3)).toBe(6);
                expect(multiply(0, 5)).toBe(0);
                expect(multiply(-2, 3)).toBe(-6);
            });

            it('should divide two numbers', () => {
                expect(divide(6, 2)).toBe(3);
                expect(divide(10, 4)).toBe(2.5);
            });

            it('should throw on division by zero', () => {
                expect(() => divide(5, 0)).toThrow('Division by zero');
            });
        });
    });
    """
  end

